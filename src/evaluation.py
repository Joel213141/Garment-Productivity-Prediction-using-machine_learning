    "    mse = metrics.mean_squared_error(y_test, predictions)\n",
    "    rmse = metrics.sqrt(mse)\n",
    "    r2 = metrics.r2_score(y_test, predictions)\n",
    "    return mae, mse, rmse, r2\n"
